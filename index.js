function happyHolidays() {
  console.log("Happy holidays!")
}
happyHolidays()

function happyHolidaysTo(name) {
  console.log(`Happy holidays, ${name}!`)
}
happyHolidaysTo('Margaret')

function happyHolidayTo(festival,name) {
  console.log(`Happy ${festival}, ${name}!`)
}
happyHolidayTo('Halloween', 'Margaret')

function holidayCountdown(festival,days) {
  console.log(`It's ${days} until ${festival}!`)
}
holidayCountdown('Halloween', 20)
